// Icon component factory
export function createIcon(name, className = '', size = 24) {
  const iconSvg = icons[name];
  if (!iconSvg) {
    console.warn(`Icon "${name}" not found`);
    return '';
  }
  
  // Replace viewBox and add size attributes
  return iconSvg.replace(
    'viewBox="0 0 24 24"',
    `viewBox="0 0 24 24" width="${size}" height="${size}" class="${className}"`
  );
}

// Alpine directive for easy icon usage
if (window.Alpine) {
  Alpine.directive('icon', (el, { expression }) => {
    const [name, size] = expression.split(',').map(s => s.trim());
    const iconSize = size ? parseInt(size) : 24;
    el.innerHTML = createIcon(name, 'alpine-icon', iconSize);
  });
}

// Export for use in JavaScript
window.clinicalIcons = { icons, createIcon };